package java_operators;

public class ComprasionOperatrors {
    public static void main(String[] args) {

        // ==
        // 2=2; 8=93;

        int num1 = 93;
        int num2 = 472;
        int num3 = 93;
        boolean compare = num1 == num3;

        System.out.println(compare);

        // !=

        boolean compare2 = 24 != 78; //(==)--> false; (!=) ---> true
        System.out.println(compare2);

        // < >

        boolean compare3 = 98 < 27;
        System.out.println(compare3);

        // >=
        boolean compare4 = 45 <= 60;
        System.out.println(compare4);


    }
}
